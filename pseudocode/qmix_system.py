class QMIXTrainer:
    """
    Orchestrates the full CTDE training loop.
    Manages: environment interaction, ε-greedy exploration, replay buffer,
    mini-batch sampling, joint loss computation, and target network updates.
    """
    def __init__(
        self,
        env,
        agent_networks: list[AgentQNetwork],
        target_agent_networks: list[AgentQNetwork],
        mixing_network: MixingNetwork,
        target_mixing_network: MixingNetwork,
        replay_buffer: ReplayBuffer,
        config: TrainingConfig,
    ):
        ...

    def select_actions(self, observations: list[Tensor], epsilon: float) -> list[int]:
        """
        ε-greedy action selection for all agents simultaneously.
        With probability ε: uniform random action (exploration)
        With probability 1-ε: greedy argmax on individual agent Q-network (exploitation)

        observations: one observation tensor per agent
        epsilon: current exploration probability
        returns: one discrete action index per agent
        Note: no communication between agents — each acts on its own observation only
        """
        ...

    def compute_loss(self, batch: dict) -> Tensor:
        """
        Computes joint QMIX TD loss over a mini-batch.

        Step 1: Forward pass online agent networks -> current Q-values for actions taken
        Step 2: Forward pass online mixing network -> current Q_tot
        Step 3: Forward pass target agent networks -> Q-values for all next actions
        Step 4: Greedy argmax per agent on target networks -> best next actions (decentralized)
        Step 5: Forward pass target mixing network with best-action Q-values -> target Q_tot
        Step 6: Compute Bellman target: r + gamma * target_Q_tot * (1 - done)
        Step 7: Smooth L1 loss between current Q_tot and Bellman target

        batch: dict with obs_i, action_i, reward, next_obs_i, done, joint_state, next_joint_state
        returns: scalar loss tensor
        """
        ...

    def soft_update_targets(self) -> None:
        """
        Polyak averaging: target_params = tau * online_params + (1-tau) * target_params
        Applied to all agent target networks and target mixing network.
        """
        ...

    def training_step(self, episode: int) -> dict:
        """
        One complete training episode:
        1. Reset environment, get initial observations
        2. Step through environment: select actions, collect transitions, store in buffer
        3. If buffer has enough samples: sample mini-batch, compute loss, gradient step
        4. Soft update target networks
        5. Decay epsilon
        Returns: dict with episode metrics (soups delivered, loss, epsilon, etc.)
        """
        ...

    def evaluate(self, num_episodes: int) -> dict:
        """
        Deterministic evaluation with epsilon=0.
        Each agent selects greedy argmax on its own Q-network only (decentralized execution).
        No mixing network needed during evaluation — only individual agent Q-networks.
        Returns: dict with mean/std soups delivered, per-agent action statistics
        """
