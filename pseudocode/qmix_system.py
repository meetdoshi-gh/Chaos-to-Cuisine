class HyperNetwork:
    """
    Generates weight/bias parameters for one layer of the mixing network.
    Conditions on joint state to produce state-dependent mixing coefficients.
    """
    def __init__(self, state_dim: int, output_shape: tuple, non_negative: bool):
        """
        state_dim: dimensionality of the joint state used to condition weights
        output_shape: (rows, cols) shape of the weight matrix to produce, or (size,) for bias
        non_negative: if True, apply non-negative activation to output (required for mixing weights)
                      if False, output is unconstrained (appropriate for bias terms)
        """
        ...

    def forward(self, joint_state: Tensor) -> Tensor:
        """
        joint_state: (batch, state_dim)
        returns: (batch, *output_shape) — weight or bias parameters for this batch
        """
        ...


class MixingNetwork:
    """
    Combines per-agent Q-values into a scalar team value Q_tot.
    All mixing weights generated by HyperNetworks conditioned on joint state.
    Monotonicity constraint (non-negative weights) enforced architecturally.
    """
    def __init__(self, num_agents: int, state_dim: int, hidden_dim: int):
        """
        num_agents: number of agents whose Q-values will be combined
        state_dim: dimensionality of joint state for hypernetwork conditioning
        hidden_dim: width of the single hidden layer in the mixing computation
        """
        ...

    def forward(self, agent_q_values: Tensor, joint_state: Tensor) -> Tensor:
        """
        Computes Q_tot via two-layer mixing with state-conditioned weights.

        agent_q_values: (batch, num_agents) — Q-value for action taken by each agent
        joint_state: (batch, state_dim) — centralized state for hypernetwork conditioning
        returns: (batch,) — scalar Q_tot per sample

        Layer 1: h = activation(batched_matmul(W1, q_values) + b1)
                 W1 shape: (batch, hidden_dim, num_agents), generated by HyperNet + non-neg activation
                 b1 shape: (batch, hidden_dim), generated by HyperNet, unconstrained
        Layer 2: Q_tot = batched_matmul(W2, h) + b2
                 W2 shape: (batch, 1, hidden_dim), generated by HyperNet + non-neg activation
                 b2 shape: (batch, 1), generated by HyperNet, unconstrained

        Monotonicity guarantee: W1 >= 0 and W2 >= 0 by construction => dQ_tot/dQ_i > 0 for all i
        """
        ...


